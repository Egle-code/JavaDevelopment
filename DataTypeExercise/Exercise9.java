package DataTypeExercise;

public class Exercise9 {
    public static void main(String[] args) {
        int num1 = 25;
        int num2 = 39;

        if (num1 == num2) {
            System.out.printf("%d == %d\n", num1, num2);
        } else {
            System.out.printf("%d != %d\n", num1, num2);
        }

        if (num1 < num2) {
            System.out.printf("%d < %d\n", num1, num2);
        }
        if (num1 > num2) {
            System.out.printf("%d > %d\n", num1, num2);
        }
        if (num1 <= num2) {
            System.out.printf("%d <= %d\n", num1, num2);
        }
        if (num1 >= num2) {
            System.out.printf("%d >= %d\n", num1, num2);

    }

}}
